from __future__ import annotations

from typing import Any

from .base import DomainPack, result_for_format, state_text


class FontSfntPack(DomainPack):
    name = "font_sfnt"

    def match(self, state: Any) -> float:
        text = state_text(state)
        return 0.82 if "harfbuzz" in text or "font" in text or "sfnt" in text or "cff" in text or "gvar" in text else 0.0

    def build(self, state: Any):
        return result_for_format(
            pack=self.name,
            score=self.match(state),
            fmt="sfnt",
            operations=[{"kind": "pad_to_length", "length": 128}],
            gaps=["needs_seed: sfnt/font template is not generated by minimal builder"],
            sanity=[{"kind": "format", "expected": "sfnt", "description": "preserve font table directory and selected table tag"}],
        )
